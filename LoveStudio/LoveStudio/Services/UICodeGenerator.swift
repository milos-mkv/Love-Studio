    private static func fmt(_ v: Double) -> String { String(format: "%.1f", v) }

    private static func blockOrComment(_ lines: [String], empty: String) -> String {
        lines.isEmpty ? "    -- \(empty)" : lines.joined(separator: "\n")
    }

    private static func textInputHandlerBody(config: UIBuilderConfig, mod: String) -> String {
        let textInputs = config.elements.filter { $0.type == .textInput }
        guard !textInputs.isEmpty else {
            return "    -- No text input widgets configured right now.\n    -- Keep this callback forwarded so future text fields work immediately."
        }

        return textInputs.map { e in
            let key = luaIdent(e.name)
            return """
    if _\(key).focused and _\(key).enabled then
        _\(key).text = _\(key).text .. t
        if \(mod).on.\(key) then \(mod).on.\(key)(_\(key)) end
    end
"""
        }.joined(separator: "\n")
    }

    private static func keyPressedHandlerBody(config: UIBuilderConfig, mod: String) -> String {
        let textInputs = config.elements.filter { $0.type == .textInput }
        guard !textInputs.isEmpty else {
            return """
    -- No text inputs configured yet.
    -- This callback is still generated so you can forward keyboard events now
    -- and add text fields later without touching main.lua again.
"""
        }

        let inputBlocks = textInputs.map { e in
            let key = luaIdent(e.name)
            return """
    if _\(key).focused and _\(key).enabled then
        if key == "backspace" then
            local byteoffset = utf8.offset(_\(key).text, -1)
            if byteoffset then
                _\(key).text = string.sub(_\(key).text, 1, byteoffset - 1)
            else
                _\(key).text = ""
            end
            if \(mod).on.\(key) then \(mod).on.\(key)(_\(key)) end
            return
        elseif key == "escape" then
            _\(key).focused = false
            return
        end
    end
"""
        }.joined(separator: "\n")

        return """
    -- Generated keyboard handling for focused text inputs.
\(inputBlocks)
"""
    }

    private static func wheelMovedHandlerBody(config: UIBuilderConfig, mod: String) -> String {
        let scrollbars = config.elements.filter { $0.type == .scrollBar }
        guard !scrollbars.isEmpty else {
            return """
    -- No scroll bars configured.
    -- Forwarding wheel input is still recommended for future UI growth.
"""
        }

        let blocks = scrollbars.map { e in
            let key = luaIdent(e.name)
            return """
    if _\(key).hovered and _\(key).enabled then
        local prev = _\(key).value
        _\(key).value = math.max(0, math.min(1, _\(key).value - dy * 0.08))
        if _\(key).value ~= prev and \(mod).on.\(key) then
            \(mod).on.\(key)(_\(key))
        end
    end
"""
        }.joined(separator: "\n")

        return """
    -- Mouse wheel updates hovered scrollbars.
\(blocks)
"""
    }

    private static func luaQuoted(_ string: String) -> String {
        let escaped = string
            .replacingOccurrences(of: "\\", with: "\\\\")
            .replacingOccurrences(of: "\"", with: "\\\"")
        return "\"\(escaped)\""
    }

    static func luaIdent(_ name: String) -> String {
        let safe = name
            .components(separatedBy: CharacterSet.alphanumerics.union(.init(charactersIn: "_")).inverted)
            .joined(separator: "_")
            .trimmingCharacters(in: CharacterSet(charactersIn: "_"))
        let result = safe.isEmpty ? "element" : safe
        return result.first?.isNumber == true ? "_\(result)" : result
    }
