--
-- HITBOXES
-- ────────
-- If hitboxes are defined in the editor they are exported per-clip per-frame.
-- Retrieve the hitbox (or all hitboxes) for the current frame at runtime:
--
--      local hb = \(module):getHitbox(player)
--      if hb then
--          -- hb.x, hb.y, hb.width, hb.height, hb.label
--      end
--
-- For collision checks, convert to world coordinates with scale:
--
--      local hb = \(module):getHitboxWorld(player, px, py, 3, 3)
--      if hb then
--          -- hb.x, hb.y, hb.width, hb.height are now in world pixels
--      end
--
--      local all = \(module):getHitboxes(player)
--
-- FRAME EVENTS
-- ────────────
-- Tag any frame in the Animation Editor with an event name (e.g. "footstep",
-- "hit", "spawn"). Pass a callback as the third argument to update() and it
-- will fire every time that frame is reached:
--
--      local function onAnimEvent(eventName, framePos, animName)
--          if eventName == "footstep" then
--              SoundManager:play("step")
--          elseif eventName == "hit" then
--              spawnHitEffect(player.x, player.y)
--          end
--      end
--
-- PER-FRAME DURATION
-- ──────────────────
-- Individual frames may carry a custom duration (seconds) that overrides the
