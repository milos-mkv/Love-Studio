--
-- SPRITE SHEET
--   Path      : \(path)
--   Frame     : \(frameWidth) × \(frameHeight) px
--   Clips     : \(config.clips.count)
--   Origin    : \(config.centerOrigin ? "center (frameWidth/2, frameHeight/2)" : "custom (\(fmt2(config.offsetX)), \(fmt2(config.offsetY)))")
--
-- HOW TO USE
-- ──────────
-- 1. Require this module at the top of the file that owns the entity:
--
--      local \(module) = require("animations/\(safeModulePath)")
--
-- 2. Load the sprite sheet once in love.load() - do this only once globally:
--
--      function love.load()
--          \(module):load()
--      end
--
-- 3. Create one instance per entity (each instance tracks its own frame/timer):
--
--      local player = \(module):newInstance("idle")
--
-- 4. Advance the animation every frame in love.update():
--
--      function love.update(dt)
--          \(module):update(player, dt, onAnimEvent)
--      end
--
-- 5. Draw the current frame in love.draw():
--
--      function love.draw()
--          \(module):draw(player, x, y)
--      end
--
-- 6. Switch to a different clip at any time (resets frame & timer):
--
--      \(module):setAnimation(player, "run")
--      \(module):setAnimation(player, "jump")
--
-- 7. Control playback speed (per-instance multiplier):
--
--      \(module):setSpeed(player, 2.0)   -- double speed
--      player.speed = 0.5               -- half speed (direct field access)
--
-- DEBUG HITBOXES
-- ──────────────
-- Enable hitbox rendering by setting the debug flag and calling drawDebug()
-- with the same position you pass to draw():
--
--      \(module).debug = true   -- toggle in love.load() or anywhere
--
--      function love.draw()
--          \(module):draw(player, x, y)
--          \(module):drawDebug(player, x, y)   -- draws on top
--      end
--
-- CALLBACKS
-- ─────────
-- Pass optional callbacks to update() for lifecycle events:
--
--      \(module):update(player, dt, onAnimEvent,
--          function(name) print("done:", name) end,    -- onComplete
--          function(name, n) print("loop", n) end      -- onLoop
